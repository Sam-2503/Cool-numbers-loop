using System;

class numbers
{
  static void Main()
  {
    Console.Write("How many lines of numbers do you want?");
    int lines = Convert.ToInt32(Console.ReadLine());
    
    for(int i = 0; i < lines; i++)
    {
      Console.WriteLine(Math.Pow(2, i);
    }
    
  }
  
}
    
